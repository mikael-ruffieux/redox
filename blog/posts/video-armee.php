          <p>Comme tout bon suisse, j’ai dû accomplir mon service militaire. On m’a affecté à la formation des blindés, des chars, dans la compagnie des explorateurs (troupes de reconnaissance).</p>
          <h2 class="section-heading">L’école de recrue</h2>
          <p>Au bout de 5 semaines à courir dans la boue, à porter un sac et des chaussures datant des années 90, le militaire professionnel chargé de sélectionner la relève des cadres me dit en entretien qu’il veut me faire grader, et je lui réponds que je trouve la formation bien assez difficile comme ça, donc la rallonger ne m’attire que très peu.</p>
          <p>Après plusieurs entretiens, un des adjudants apprend que je fais de la vidéo, et me vend cette promotion comme une éventuelle opportunité pour moi de faire de la vidéo à l’armée.</p>
          
          
          <img class="img-fluid" src="/blog/img/armee1.jpg" alt="Eagles avant un exercice">
          <span class="caption text-muted">L'Eagle, le 4x4 de 5 tonnes des explorateurs. Photo prise avant le départ d'un exercice.</span>

          <p>Quelle blague.</p>
          <p>École de sous-officier : pas de vidéo, service pratique en tant que sergent : pas de vidéo, école de sous-officier supérieur : pas de vidéo … je crois que vous avez compris le pattern.</p>
          
          <h2 class="section-heading">Premier cours de répétition</h2>
          <p>Pour ceux qui ne sont pas familiers avec le système militaire suisse, une fois l’école de recrue terminée, les militaires sont affectés à un bataillon, où il devra effectuer une fois par année 3 à 4 semaines de service, pour s’entrainer et mettre à jour ses compétences militaires.</p>
          <p>Je rejoins donc le bataillon des sapeurs de chars 1, en tant que sergent-major d’unité (responsable logistique et RH d’une compagnie). Le cours se déroule sans soucis, et j’apprends à la fin du cours l’existence d’une « cellule de communication » dans le bataillon.</p>
          <p>Intéressé, je contacte directement l’officier responsable de cette cellule, qui est directement intéressé par mon profil, mais redevient prudent quand il apprend ma fonction : il est facile pour lui de muter un soldat, ou même un sergent au sein de sa cellule, mais un sergent-major chef, il n’y en a qu’un dans une compagnie.</p>
          <p>Malheureusement, après plusieurs échanges avec mon commandant de compagnie, ce dernier refuse de me muter, et me propose un entre-deux, où je pourrais travailler à temps partielle comme vidéaste, mais devrait toujours assumer mes fonctions de sergent-major.</p>
          
          <h2 class="section-heading">Après 368 jours : « Et action ! »</h2>
          <p>Mon deuxième cours de répétition commence, et j’entre directement en contact avec l’officier spécialiste (Le « PIO »), chef de la cellule comm’. Le courant passe très bien, et je suis rapidement envoyé sur le terrain, armé cette fois de mon appareil photo, et non plus de mon fusil d’assaut.</p>
          <p>Je rejoins sur plusieurs exercices la section d’explorateurs du bataillon, dirigée par un ancien camarade, le Lt Lanners, qui m’intègre non pas seulement comme vidéaste, mais comme assistant à la direction de ses exercices.</p>
          <p>C’est un rêve qui devient réalité : je peux filmer à l’armée, et filmer ma fonction initiale, celle des explorateurs !</p>
          
          
          <img class="img-fluid" src="/blog/img/lanners.jpg" alt="Lt Lanners">
          <span class="caption text-muted">Le Lt Lanners en exercice : mouvement sous feu ennemi.</span>

          <h2 class="section-heading">En conclusion: when life gives you lemons …</h2>
          <p>Make lemonade.</p>
          <p>L’armée est un monde très statique, et s’il m’a fallu autant de temps pour enfin arriver à mes fins, ce n’est qu’à force de compromis que j’ai pu y arriver. Une fois qu’on connaît le système administratif militaire, c’est beaucoup plus simple de s’en sortir, et si on arrive à convaincre les bonnes personnes, on peut aller loin.</p>
          <p>Et en déplaise à ce fameux adjudant, j’ai pu filmer à l’armée, et sans son aide.</p>
          <p>À l’issue de ce cours, on m’a proposé de reprendre la cellule communication d’un autre bataillon, ce qui m’aurait fortement intéressé, mais ça impliquait de prendre un nouveau grade, et d’ajouter 240 jours de service à mon palmarès militaire. J’ai donc refusé, j’ai d’autres choses à faire que d’être convoqué en cours de répétition jusqu’à mes 35 ans.</p>
