            <p class="c1"><span class="c3">On peut d&eacute;j&agrave; voir qu&rsquo;initialement, nos hypoth&egrave;ses sur le sujet
                    rejoignent les avis des protagonistes quotidiens de l&rsquo;&eacute;cole. Un autre point est ressorti des
                    entretiens : la possibilit&eacute; de plus moduler ses &eacute;tudes, et d&rsquo;&ecirc;tre non seulement
                    acteur, mais planificateur de ses &eacute;tudes. </span></p>
            <p class="c1"><span class="c3">Mais il est important de prendre en compte l&rsquo;&eacute;chantillon
                    &eacute;tudi&eacute; : il s&rsquo;agissait de deux &eacute;tudiants, qui ne connaissent pas forc&eacute;ment
                    tous les facteurs influant la mise en place de telle ou telle option dans la formation. Par exemple, la
                    pr&eacute;sence obligatoire aux cours est une directive de la HES-SO, et non de la fili&egrave;re.</span></p>
             
            <h2 class="c14" id="h.xs0fptuzbu32"><span class="c9">R&eacute;sultats &amp; recommandations</span></h2>
            <p class="c1"><span class="c3">En mettant en comparaison les donn&eacute;es r&eacute;colt&eacute;es dans nos entretiens,
                    et les diff&eacute;rentes sources scientifiques d&eacute;couvertes &nbsp;dans la recherche documentaire, on
                    &nbsp;voit que nos hypoth&egrave;ses sont bien fond&eacute;es.</span></p>
             
            <p class="c1"><span class="c3">Comme recommandations, nous proposerions &agrave; l&rsquo;&eacute;cole de se pencher sur
                    le d&eacute;veloppement d&rsquo;une formation en emploi pour la fili&egrave;re d&rsquo;IM. Concernant la
                    pr&eacute;sence aux cours obligatoire, il s&rsquo;av&egrave;re que cela d&eacute;pend non pas de la
                    fili&egrave;re, mais de la HES-SO, et que la d&eacute;cision ne revient du coup pas aux responsables de la
                    fili&egrave;re. Finalement, la possibilit&eacute; de mettre en place des options de formation plus t&ocirc;t
                    dans le cursus pourrait aussi &ecirc;tre d&rsquo;un grand attrait pour les futurs &eacute;tudiants.</span></p>
             
