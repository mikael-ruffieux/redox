            <h2 class="c14" id="h.t6pd0phk8deq"><span>Introduction</span></h2>
            <p class="c1">Dans le cours d&rsquo;Analyse et Recherche dispens&eacute; &agrave; la HEIG-VD, il nous a
                    &eacute;t&eacute; demand&eacute; de produire un rapport de recherche scientifique sur une probl&eacute;matique
                    d&eacute;j&agrave; &eacute;tudi&eacute;e en classe. J&rsquo;ai choisi la probl&eacute;matique suivante : comment
                    am&eacute;liorer la fili&egrave;re d&rsquo;Ing&eacute;nierie des M&eacute;dias ?</p>
             
            <p class="c1"><span class="c3">Avant de se lancer dans le vif du sujet, j&rsquo;ai r&eacute;alis&eacute; une
                    premi&egrave;re recherche documentaire &agrave; la recherche de chiffres concernant la fili&egrave;re
                    d&rsquo;Ing&eacute;nierie des M&eacute;dias (IM) et les autres fili&egrave;res d&rsquo;ing&eacute;nierie et
                    d&rsquo;architecture propos&eacute;es au sein de la HES-SO. J&rsquo;ai aussi regroup&eacute; une s&eacute;rie de
                    rapports scientifiques et journalistiques sur certaines tendances p&eacute;dagogiques, notamment celles des pays
                    scandinaves, pays &agrave; la pointe de l&rsquo;&eacute;ducation.</span></p>
             
            <h2 class="c14" id="h.3ycpgg661sec"><span class="c9">Objectifs de recherche</span></h2>
            <p class="c1"><span class="c3">Ce rapport a pour but de mettre en exergue certains &nbsp;points &agrave;
                    &nbsp;am&eacute;liorer au sein de la &nbsp;fili&egrave;re &nbsp;d&rsquo;IM, et de pr&eacute;senter des
                    &eacute;ventuelles solutions alternatives aux diff&eacute;rentes probl&eacute;matiques soulev&eacute;es par les
                    donn&eacute;es r&eacute;colt&eacute;es.</span></p>
             
            <p class="c1"><span class="c3">La recherche se voudra qualitative, et orient&eacute;e autour de deux axes :
                    l&rsquo;&eacute;tude en emploi, et la pr&eacute;sence obligatoire aux cours.</span></p>
             
            <h2 class="c14" id="h.ef5i4xk9e9io"><span class="c9">Recherche documentaire</span></h2>
            <p class="c1"><span class="c3">Afin de poser un contexte pertinent, une recherche documentaire pr&eacute;alable
                    &eacute;tait n&eacute;cessaire. J&rsquo;ai consult&eacute; divers ouvrages traitant des diff&eacute;rentes
                    m&eacute;thodes d&rsquo;enseignement dans le monde, des diff&eacute;rents statistiques qui existent sur la
                    fili&egrave;re et finalement sur le rapport qu&rsquo;ont les &eacute;tudiants avec le travail vis-&agrave;-vis
					de leurs &eacute;tudes.</span>
			</p>
			
			<img class="img-fluid" alt="Statistiques de la HES-SO" src="/blog/img/statistiques-HES-SO.png">
          	<span class="caption text-muted">Les statistiques de la HES-SO.</span>


            <p class="c1"><span class="c3">Depuis les quatre derni&egrave;res ann&eacute;es, la fili&egrave;re d&rsquo;IM
                    poss&eacute;dait un effectif relativement stable, avec en moyenne 90 &eacute;tudiants par ann&eacute;e. Ce
                    chiffre a subitement grimp&eacute; &agrave; 103 en 2019, cassant &nbsp;la &nbsp;tendance. On peut expliquer ce
                    &nbsp;saut gr&acirc;ce &agrave; la &nbsp;cr&eacute;ation d&rsquo;une maturit&eacute; sp&eacute;cialis&eacute;e
                    &nbsp;en communication et information (MSCI), &nbsp;qui a pour but de pr&eacute;parer les &eacute;l&egrave;ves
                    &nbsp;du &nbsp;gymnase aux fili&egrave;res d&rsquo;IM, &nbsp;d&rsquo;informatique de gestion et de tourisme.
                </span></p>
            <p class="c1"><span class="c3">L&rsquo;&Eacute;tat de Vaud met donc en place &nbsp;des &nbsp;moyens &nbsp;pour
                    &nbsp;augmenter l&rsquo;attrait de la fili&egrave;re d&rsquo;IM, mais est-ce que cette fili&egrave;re est
                    pr&ecirc;te &agrave; recevoir plus de monde ? En effet, on remarque que par rapport aux &nbsp;effectifs &nbsp;en
                    Ing&eacute;nierie en Informatique, qui sont 5 fois plus &nbsp;grands, l&rsquo;IM &nbsp;ne demande pas les
                    m&ecirc;mes infrastructures, et n&rsquo;est pas forc&eacute;ment pr&ecirc;te &agrave; accueillir autant de
                    nouveaux &eacute;tudiants.</span></p>
             
            <p class="c1"><span class="c3">La r&eacute;ponse &agrave; cette question de quantit&eacute; d&rsquo;&eacute;tudiants a
                    d&eacute;j&agrave; &eacute;t&eacute; r&eacute;pondu par des universit&eacute;s comme par exemple l&rsquo;EPFL :
                    pr&eacute;sence au cours non-contr&ocirc;l&eacute;e, et cours donn&eacute;s en amphith&eacute;&acirc;tres. Une
                    autre solution serait d&lsquo;augmenter la dur&eacute;e des &eacute;tudes et baisser le pourcentage des cours
                    sur la semaine, en proposant une formation en emploi, comme il est d&eacute;j&agrave; propos&eacute; dans de
                    nombreuses fili&egrave;res de la HEIG-VD, et de la HES-SO de mani&egrave;re plus g&eacute;n&eacute;rale.
                    C&rsquo;est sur ces 2 &nbsp;questionnements que se d&eacute;roulera notre analyse.</span></p>
             
            <p class="c1"><span class="c3">Pour le premier axe, on nous indique dans une &eacute;tude qu&eacute;b&eacute;coise que
                    la formation &agrave; distance &ldquo;[...]&nbsp;est un succ&egrave;s qui va au-del&agrave; de mes attentes, les
                    &eacute;tudiants sont tr&egrave;s enthousiastes, ils d&eacute;posent des travaux vari&eacute;s et de
                    qualit&eacute;.&rdquo; (Baillargeon, 2019). Mais il est clair que l&rsquo;aspect de m&eacute;thodologie y est
                    tr&egrave;s important. Pour arriver &agrave; des r&eacute;sultats concluants, il est important que les
                    &eacute;tudiants sachent travailler d&rsquo;eux-m&ecirc;mes.</span></p>
             
            <p class="c1"><span class="c3">Et pour le deuxi&egrave;me axe, les avis divergent dans le monde scientifique. la
                    formation en emploi, on nous rapporte que &ldquo;[les statistiques] montrent que, pour les lyc&eacute;ens
                    canadiens, un emploi r&eacute;gulier intensif de plus de 30 heures par semaine diminue significativement les
                    probabilit&eacute;s de r&eacute;ussite scolaire et de poursuite des &eacute;tudes.&rdquo; (B&eacute;duw&eacute;,
                    2004). Si ces chiffres ne &nbsp;sont pas contredits, une &eacute;tude fran&ccedil;aise indique que
                    &nbsp;&ldquo;[...] les habitudes, les comp&eacute;tences, les comportements que les jeunes peuvent
                    acqu&eacute;rir dans leur travail sont &eacute;galement appr&eacute;ci&eacute;s dans le syst&egrave;me
                    scolaire.&rdquo; et que &ldquo;[...]&nbsp;les jeunes travaillant durant leurs &eacute;tudes peuvent allouer leur
                    temps de mani&egrave;re plus efficace, de fa&ccedil;on &agrave; r&eacute;duire les cons&eacute;quences
                    n&eacute;gatives sur le temps scolaire. Le type d&rsquo;emploi peut &eacute;galement avoir une influence sur la
                    r&eacute;ussite scolaire.&rdquo; (Beffy, 2009).</span></p>
             
            <h2 class="c14" id="h.5nptolfpelod"><span class="c9">Probl&eacute;matique</span></h2>
            <p class="c1"><span class="c3">Comment am&eacute;liorer la fili&egrave;re d&rsquo;Ing&eacute;nierie des M&eacute;dias ?
                    &nbsp;Les r&eacute;ponses &agrave; cette probl&eacute;matique sont pos&eacute;es hypoth&eacute;tiquement de la
                    mani&egrave;re suivante : 1. Baisser le taux de pr&eacute;sent&eacute;isme aux cours ex cathedra et favoriser
                    l&rsquo;&eacute;tude &agrave; distance. 2. Proposer une formation en emploi &agrave; un pourcentage moins
                    &eacute;lev&eacute;, permettant aux &eacute;tudiants de se familiariser directement avec le monde du
                    travail.</span></p>
             
            <h2 class="c14" id="h.kjseato4gap2"><span class="c9">R&eacute;colte de donn&eacute;es</span></h2>
            <p class="c1"><span class="c3">Pour soutenir cette th&egrave;se, nous avons organis&eacute; un entretien personnel avec
                    deux &eacute;tudiants de la fili&egrave;re. En effet, pour faire la proposition d&rsquo;une solution
                    qualitative, l&rsquo;entretien &eacute;tait pour nous la meilleure solution, nous donnant le meilleur rapport
                    quantit&eacute; d&rsquo;informations / moyen mis en place pour une r&eacute;colte de donn&eacute;es qualitative
                    sur le sujet.</span></p>
             
            <p class="c1"><span class="c3">Les deux personnes interrog&eacute;es poss&eacute;daient chacune un profil assez
                    vari&eacute;, le premier sujet &eacute;tant un homme d&rsquo;une vingtaine d&rsquo;ann&eacute;e, ayant peu
                    d&rsquo;exp&eacute;rience professionnelle dans le domaine, et le deuxi&egrave;me sujet &eacute;tant une femme
                    d&rsquo;une trentaine d&rsquo;ann&eacute;es ayant travaill&eacute;e pendant 10 ans dans le domaine avant de
                    commencer ses &eacute;tudes dans la fili&egrave;re IM.</span></p>
             
            <p class="c1"><span class="c3">Afin de mieux diriger l&rsquo;entretien, une grille d&rsquo;&eacute;valuation avait
                    &eacute;t&eacute; pr&eacute;par&eacute;e au pr&eacute;alable, avec quelques questions d&rsquo;approfondissement
                    guidant la discussion vers nos deux hypoth&egrave;ses &eacute;tablies initialement.</span></p>
             
            <p class="c1"><span class="c3">Lors de l&rsquo;entretien, une fois que le contexte eut &eacute;t&eacute; expliqu&eacute;
                    aux participants, ils nous ont donn&eacute; leurs avis sur le sujet, par rapport &agrave; leurs
                    exp&eacute;riences pass&eacute;es. Une expression nous a particuli&egrave;rement marqu&eacute;e :
                    &ldquo;l&rsquo;&eacute;tudiant doit &ecirc;tre acteur de sa formation&rdquo;. En effet, cette id&eacute;e
                    d&rsquo;&eacute;tudiant actif dans sa formation a &eacute;t&eacute; mentionn&eacute;e &agrave; de nombreuses
                    reprises par les deux &eacute;tudiants. L&rsquo;&eacute;cole, selon eux, manquait de flexibilit&eacute; et avait
                    de la peine &agrave; donner des responsabilit&eacute;s &agrave; ses &eacute;tudiants, et les maternait, tout en
                    exigeant d&rsquo;eux de mani&egrave;re antith&eacute;tique d&rsquo;&ecirc;tre responsables et proactifs.</span>
            </p>
             
            <p class="c1"><span class="c3">Enfin, pour les deux r&eacute;pondants se rejoignaient sur l&rsquo;id&eacute;e de pouvoir
                    effectuer la formation d&rsquo;IM en cours d&rsquo;emploi, malgr&eacute; leurs diff&eacute;rentes
                    exp&eacute;riences professionnelles. En effet, les participants estimaient &agrave; l&rsquo;unanimit&eacute;
                    qu&rsquo;avoir des contacts dans le domaine des m&eacute;dias durant les &eacute;tudes &eacute;tait un immense
                    avantage, et permettrait de rendre certains cours plus concrets, en confrontant directement les connaissances
                    acquises en cours &agrave; la r&eacute;alit&eacute; du monde du travail.</span></p>
             
